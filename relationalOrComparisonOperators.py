# Relational or Comparison or Conditional Operators!

"""In Programming when we compare any to values we use Operators as Given below"""

Point1 = 59

Point2 = 37

print(Point1 == Point2) # False! because they are not equal.

print(Point1 != Point2) # True! because they are not equal.

print(Point1 > Point2) # True! because Point1 is greater than Point2.

print(Point1 < Point2) # False! because Point1 is not less than Point2.

print(Point1 >= Point2) # True! because Point1 is greater than Point2.

print(Point1 <= Point2) # False because Point1 is not less than Point2.
